package main;

/**
 * Class for adding two numbers.
 * 
 * @author somesh
 *
 */
@SuppressWarnings("checkstyle:HideUtilityClassConstructor")
public final class AddTwoNumbers {
  /**
   * Static Method to add two numbers.
   * 
   * @param a int
   * @param b int
   * @return int
   */
  public static int add(int a, int b) {
    return a + b;
  }
}
